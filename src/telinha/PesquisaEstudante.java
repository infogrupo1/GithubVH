    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        //cria o dao para acesso bd
        EstudanteDAO dao = new EstudanteDAO();
        //chama o método de recuperação e armazena o retorno
        listaBanco = dao.pesquisaEstudante(txtPesquisa.getText());
        //mostrar na tabela, recuperar o model da jTable
        DefaultTableModel model = (DefaultTableModel) tblPesquisa.getModel();
        //limpa tabela
        model.setNumRows(0);
        //percorrer a lista de retorno
        for (Estudante e: listaBanco){
            //usar o model para add uma linha na tabela
            model.addRow(new Object[]{e.getNome(), e.getTelefone()});

        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        //verificar a linha selecionada na tabela
        int linha = tblPesquisa.getSelectedRow();
        System.out.println(linha);
        if(linha < 0){
            JOptionPane.showMessageDialog(this, "Selecione um curso na tabela!!!");
        }else{
            //recupera o id do cursop selecionado
            int id = listaBanco.get(linha).getIdEstudante();
            //confirmar a exclusão
            int opcao = JOptionPane.showConfirmDialog(this, "tem certeza que quer mesmo excluir?");
            if(opcao == 0){
                //em caso de confirmação vai chamar o excluir
                EstudanteDAO dao = new EstudanteDAO();
                //recuperar o retorno da exclusao
                boolean retorno = dao.excluirEstudante(id);
                //atualizar a gui
                if(retorno){
                    DefaultTableModel model = (DefaultTableModel) tblPesquisa.getModel();
                    model.removeRow(linha);
                    JOptionPane.showMessageDialog(this, "excluido com sucesso");
                }else{
                    JOptionPane.showMessageDialog(this, "erro na exclusao");
                }
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

